function getTrackIds() {
  return Object.keys(TRACKS);
}

function getRandomTrackId(seed = 1) {
  const trackIds = getTrackIds();
  const safeSeed = Number.isFinite(seed) ? Math.abs(Math.floor(seed)) : 1;
  return trackIds[safeSeed % trackIds.length];
}

export function getTrack(trackId, seed = 1) {
  if (!trackId || trackId === "random") {
    return TRACKS[getRandomTrackId(seed)];
  }
  return TRACKS[trackId] || TRACKS[getRandomTrackId(seed)];
}
